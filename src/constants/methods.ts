export enum HTTPMethods {
  GET = "GET",
  POST = "POST",
  PUT = "PUT",
  PATCH = "PATCH",
  DELETE = "DELETE"
}
// Exporting an enumeration (enum) called `HTTPMethods` that defines the common HTTP methods.
// Each member of the enum is assigned a string value corresponding to the HTTP method it represents.

// - `GET`: Used to request data from a specified resource.
// - `POST`: Used to send data to a server to create/update a resource.
// - `PUT`: Used to update an existing resource or create a new resource if it doesn't exist.
// - `PATCH`: Used to apply partial modifications to a resource.
// - `DELETE`: Used to delete a specified resource.
